/*  C++ Program to Addition of Two Numbers  */

#include<iostream>
using namespace std;

int main()

{
    int a,b,sum;
    
    cout<<"\n Enter the first number a = ";
    cin>>a;
    
    cout<<"\n Enter the second number b =";
    cin>>b;
    
    sum=a+b;
    
    cout<<"\n Sum of a & b = "<<sum;
    
    return 0;
}
